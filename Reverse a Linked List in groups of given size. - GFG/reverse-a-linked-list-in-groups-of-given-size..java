class Solution
{
    public static Node reverse(Node head, int k)
    {
        if(head == null || head.next == null || k == 0) return head;
        Node curr = head, prev = null, fwd = null;
        for(int i = 0; i < k && curr != null; i++) {
            fwd = curr.next;
            curr.next = prev;
            prev = curr;
            curr = fwd;
        }
        if(curr != null) head.next = reverse(curr, k);
        return prev;
        
    }
}

